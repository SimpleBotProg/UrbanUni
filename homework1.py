#string индексация строк

example = 'HelloWorld'
print(example[0])       #первый символ этой строки
print(example[-1])      #последний символ этой строки (используя отрицательный индекс)
print(example[5::])     #вторую половину этой строки (с нечётным количеством символов)
print(example[::-1])    #это слово наоборот
print(example[1:10:2])  #каждый второй символ этой строки

